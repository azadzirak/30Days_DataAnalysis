CREATE TABLE sales (
    id INI PRIMARY KEY,      -- شماره یکتا برای هر خرید
    customer_name TEXT,      -- اسم مشتری
    product TEXT,            -- محصول
    quantity INT,            -- تعداد
    price FLOAT,             -- قیمت واحد
    date DATE                -- تاریخ خرید
);

-- این بخش برای درست کردن یه قالب جدیده احتمالا و به ترتیب اسم المان ها و نوع و تایپشون رو نوشتیم.
-- این دستور می گه یه جدول به این اسم درست کن که این ها ستون هاش باشن.
-- id کلید اصلی باشه یعنی هر ردیف یکتا.

INSERT INTO sales VALUES 
